PROC CALCX       ; calculates 16 steps between min and max for the X-Axis
  call getxaxis
  cmp joyminleft,eax
  jb short L1
  mov joyminleft,eax
L1:
  cmp joymaxright,eax
  ja short L2
  mov joymaxright,eax
L2:
  ; now calculate 16 steps
  mov ebx,joymaxright
  sub ebx,joyminleft
  ; in ebx joystick Range
  sub eax,joyminleft
  ; in eax current Position
  shr ebx,4
  ; ebx is range div 16
  cmp ebx,0
  je short nodiv
  xor edx,edx
  div ebx
nodiv:
  ; in eax a number between 0 .. 15
  ret
ENDP CALCX

PROC CALCY       ; calculates 16 steps between min and max for the Y-Axis
